def tup_function(value1, value2):
    tup = (value1, value2)
    return tup

print(tup_function(5, "taco"))

def lst_function(value3, value4):
    lst = [value3, value4]
    return lst

print(lst_function(5, "taco"))

def dty_function(value4, value5):
    dty = dict()
    dty[value4] = value5
    return dty

print(dty_function(5, "taco"))

